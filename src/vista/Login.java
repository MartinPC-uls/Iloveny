    private void userInputFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_userInputFocusLost
        String text = userInput.getText();
        if (text.length() == 0) {
            downPanelUser.setBackground(Color.RED);
        } else {
            downPanelUser.setBackground(Color.GREEN);
        }
    }//GEN-LAST:event_userInputFocusLost

    private void passwordInputFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passwordInputFocusLost
        String text = passwordInput.getText();
        if (text.length() == 0) {
            downPanelPassword.setBackground(Color.RED);
        } else {
            downPanelPassword.setBackground(Color.GREEN);
        }
    }//GEN-LAST:event_passwordInputFocusLost

